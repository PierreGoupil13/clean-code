package ex1;

/**
 * Cette classe fournit des méthodes pour effectuer des opérations arithmétiques simples.
 */
public class Calcul {

    /**
     * Retourne la somme de deux nombres.
     *
     * @param a Le premier nombre.
     * @param b Le deuxième nombre.
     * @return La somme de a et b.
     */
    public int additionner(int a, int b) {
        return a + b;
    }

    /**
     * Retourne la différence entre deux nombres.
     *
     * @param a Le premier nombre (le minuend).
     * @param b Le deuxième nombre (le subtrahend).
     * @return La différence entre a et b.
     */
    public int soustraire(int a, int b) {
        return a - b;
    }
}

